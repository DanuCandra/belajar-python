# operasi aritmatika

a = 10
b = 3

# operasi tambah +
hasil = a + b
print(a, "+", b, "=", hasil)

# operasi pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# operasi kali *
hasil = a * b
print(a, "*", b, "=", hasil)

# operasi pembagian /
hasil = a / b
print(a, "/", b, "=", hasil)

# operasi eksponen (pangkat) **
hasil = a ** b
print(a, "**", b, "=", hasil)

# operasi modulus / sisa bagi %
hasil = a % b
print(a, "%", b, "=", hasil)

# operasi floor division //
hasil = a // b
print(a, "//", b, "=", hasil)

